#include "matmul/matrix.h"

#include <gtest/gtest.h>

#include <array>
#include <cstddef>
#include <vector>

void expect_matrix_near(Matrix &actual, std::size_t rows, std::size_t cols,
                        const std::vector<float> &expected, float tolerance = 1e-5f)
{
    ASSERT_EQ(actual.shape(), (std::array<std::size_t, 2>{rows, cols}));
    ASSERT_EQ(expected.size(), rows * cols);

    for (std::size_t row = 0; row < rows; ++row)
    {
        for (std::size_t col = 0; col < cols; ++col)
        {
            EXPECT_NEAR(actual(row, col), expected[row * cols + col], tolerance)
                << "Mismatch at row " << row << ", column " << col;
        }
    }
}

TEST(MatrixTest, MultipliesSquareMatrices)
{
    Matrix A(5, 5,
             {
                 2, -1, 3, 0, 4, 5, 2, -2, 1, 3, -3, 4, 1, 2, -1, 0, 6, -4, 3, 2, 7, -2, 5, -3, 1,
             });
    Matrix B(5, 5,
             {
                 1, 3, -2, 4, 0, -4, 2, 5, -1, 3, 6, 0, -3, 2, -2, 2, -5, 1, 3, 4, -1, 4, 2, -2, 5,
             });

    std::vector<float> C_E = {
        20, 20, -10, 7, 11, -16, 26, 13, 11, 29,  -8, -15, 23,
        -6, 13, -44, 5, 49, -9,  48, 38, 36, -40, 29, -23,
    };

    Matrix C_1 = A.matmul_ijk(B);
    Matrix C_2 = A.matmul_ikj(B);
    Matrix C_3 = A.matmul_blocked_ikj(B, 1);
    Matrix C_4 = A.matmul_parallel_blocked_ikj(B, 1);

    expect_matrix_near(C_1, 5, 5, C_E);
    expect_matrix_near(C_2, 5, 5, C_E);
    expect_matrix_near(C_3, 5, 5, C_E);
    expect_matrix_near(C_4, 5, 5, C_E);
}

TEST(MatrixTest, MultipliesRectangularMatrices)
{
    Matrix A(3, 4,
             {
                 1.5f,
                 -2,
                 0.5f,
                 3,
                 -1,
                 4,
                 2.5f,
                 0,
                 3,
                 -0.5f,
                 -2,
                 1,
             });
    Matrix B(4, 2,
             {
                 2,
                 -1,
                 0.5f,
                 3,
                 -4,
                 2,
                 1.5f,
                 -2,
             });
    std::vector<float> C_E{
        4.5f, -12.5f, -10, 18, 15.25f, -10.5f,
    };

    Matrix C_1 = A.matmul_ijk(B);
    Matrix C_2 = A.matmul_ikj(B);
    Matrix C_3 = A.matmul_blocked_ikj(B, 1);
    Matrix C_4 = A.matmul_parallel_blocked_ikj(B, 1);

    expect_matrix_near(C_1, 3, 2, C_E);
    expect_matrix_near(C_2, 3, 2, C_E);
    expect_matrix_near(C_3, 3, 2, C_E);
    expect_matrix_near(C_4, 3, 2, C_E);
}

